def floor(n: float) -> int:
    """
    Return the floor of a floating-point number.

    The floor of a number is the largest integer less than or equal to it.

    Args:
        n (float): The number to be floored.

    Returns:
        int: The floor of the number.
    """
    return math.floor(n)
